function checkTel(){
	var tel =  $.trim($(".input-tel").val());
	if (null === tel || "" === tel || tel.length === 0 || tel==='请输入11位手机号') {
		 ucar.uitls.show("手机号填写错误<span class='face face-3'></span>");
		return false;
	}
	if(! mobileValidate(tel) ){
		ucar.uitls.show("手机号填写错误<span class='face face-3'></span>");
		return false;
	}else{
		return true;
	}
};

